    def database_total_data(self):
        connect = sqlite3.connect("Databases\\LTO.db")
        c = connect.cursor()

        c.execute(f"SELECT * FROM LTO")
        db_extract = c.fetchall()
        open_opportunities = len(db_extract)

        # LTO Total EURk SUM
        c.execute(f"SELECT SUM(sales) FROM LTO")
        db_extract = c.fetchall()
        if not db_extract[0][0]:
            lto_total = '0'
        else:
            lto_total = format(db_extract[0][0], ",")

        # Winnable COUNT
        c.execute(f"SELECT * FROM LTO WHERE status LIKE 'W%'")
        db_extract = c.fetchall()
        winnable_count = len(db_extract)

        # Winnable SUM
        c.execute(f"SELECT SUM(sales) FROM LTO WHERE status LIKE 'W%'")
        db_extract = c.fetchall()
        if not db_extract[0][0]:
            winnable_sum = '0'
        else:
            winnable_sum = format(db_extract[0][0], ",")

        # LTO Outstanding COUNT
        c.execute(f"SELECT * FROM LTO WHERE status LIKE 'D%'")
        db_extract = c.fetchall()
        lto_doubt_count = len(db_extract)

        # LTO Outstanding SUM
        c.execute(f"SELECT SUM(sales) FROM LTO WHERE status LIKE 'D%'")
        db_extract = c.fetchall()
        if not db_extract[0][0]:
            lto_doubt_sum = '0'
        else:
            lto_doubt_sum = format(db_extract[0][0], ",")

        # LTO Outstanding Uncommented
        c.execute(f"SELECT * FROM LTO WHERE comment = 'None'")
        db_extract = c.fetchall()
        lto_uncommented = len(db_extract)

