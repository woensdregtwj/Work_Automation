    def customer_diversity(self, kam):
        # We want to sum the total amount for each customer, concatenating the JM.
        cus_diversity = {}
        jm = []
        labels = []

        # Data structure: {Customer: JM amount}
        # We want percentages, so the calculation is jm amount / total jm amount * 100

        for cell in range(3, self.ws_all.max_row +1):
            if self.ws_all.cell(row=cell, column=10).value == "SQLCOPY":
                if self.ws_all.cell(row=cell, column=12).value == kam:
                    customer = self.ws_all.cell(row=cell, column=11).value
                    jm_value = self.ws_all.cell(row=cell, column=52).value
                    if not jm_value or jm_value < 0:
                        jm_value = 0  # For avoiding value errors

                    cus_diversity.setdefault(customer, 0)
                    cus_diversity[customer] += jm_value

        #labels, jm = zip(*cus_diversity.items())

        original_labels = [i for i in cus_diversity.keys()]
        jm = [i for i in cus_diversity.values()]
        print(jm)

        # For creating percentages that we will show in the legend
        total_jm = 0
        for number in jm:
            total_jm += number
        percentages = [round(float(p / total_jm * 100),1) for p in jm]
        print(percentages)

        for number in range(len(jm)):
            if (jm[number] / total_jm) * 100 > 5:
                labels.append(original_labels[number])
                continue
            labels.append('')
        print(labels)

        # Setting up the chart
        diversity_figure = Figure()
        diversity_canvas = FigureCanvasQTAgg(diversity_figure)
        diversity_plot = diversity_figure.add_subplot(111)

